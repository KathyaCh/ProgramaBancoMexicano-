    private void jbtnContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnContinuarActionPerformed
        // TODO add your handling code here:
        //Agregar datos a la caja de texto
        String o = jtxtfOpcion.getText();
        int opcion = Integer.parseInt (o); 
        
        //Agregar switch para que se ejecuten los casos segun la opcion que ingrese el usuario 
        switch (opcion) {
            case 1:
                DepositoJFrame deposito = new DepositoJFrame ();
                deposito.show();
                break;
            case 2:
                RetiroJFrame retiro = new RetiroJFrame ();
                retiro.show();
                break; 
            case 3:
                SaldoJFrame saldo = new SaldoJFrame ();
                saldo.show();
                break;
            case 4:
                this.dispose();
                break;
            default: 
                JOptionPane.showMessageDialog(null, "Opci칩n no v치lida");
                break;
                
        }
        jtxtfOpcion.setText("");
        
    }//GEN-LAST:event_jbtnContinuarActionPerformed

